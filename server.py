from fastapi.responses import RedirectResponse

@app.get("/", include_in_schema=False)
async def root():
    """Redirect root to OpenAPI docs."""
    return RedirectResponse(url="/docs")

@app.get("/health", response_model=HealthResponse, tags=["System"])
async def health():
    """Health check endpoint. Returns 200 OK."""
    return HealthResponse()


@app.post("/reset", response_model=ResetResponse, tags=["Environment"])
async def reset(request: ResetRequest = None):
    """
    Initialise a new audit episode.

    Returns the initial observation with the first turn to audit.
    """
    if request is None:
        request = ResetRequest()

    try:
        obs = _env.reset(task_id=request.task_id, seed=request.seed)
        return ResetResponse(
            observation=obs.model_dump(),
            done=False,
        )
    except ValueError as e:
        raise HTTPException(status_code=400, detail=str(e))


@app.post("/step", response_model=StepResponse, tags=["Environment"])
async def step(request: StepRequest):
    """
    Submit the agent's decision for the current turn.

    Returns the next observation, reward, done flag, and info dict.
    """
    try:
        decision = Decision(request.decision.upper())
    except ValueError:
        raise HTTPException(
            status_code=400,
            detail=f"Invalid decision: {request.decision}. Must be ALLOW or BLOCK.",
        )

    action = AuditAction(decision=decision, reasoning=request.reasoning)

    try:
        next_obs, reward, done, info = _env.step(action)
        return StepResponse(
            observation=next_obs.model_dump() if next_obs else None,
            reward=reward.model_dump(),
            done=done,
            info=info,
        )
    except RuntimeError as e:
        raise HTTPException(status_code=400, detail=str(e))


@app.get("/state", tags=["Environment"])
async def state():
    """Returns the current serialisable environment state."""
    return _env.state()


