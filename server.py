"""
FastAPI server for the Trust & Safety Audit Environment.

Exposes all 8 required endpoints:
  /reset    POST  — Initialise new episode
  /step     POST  — Submit action, get observation + reward
  /state    GET   — Current environment state
  /tasks    GET   — List all tasks + action schema
  /grader   GET   — Return grader score after episode
  /baseline POST  — Trigger baseline and return scores
  /health   GET   — Returns 200 OK
  /docs     GET   — OpenAPI docs (auto-generated by FastAPI)
"""

from __future__ import annotations

import asyncio
import json
import os
from typing import Any, Dict, List, Optional

from fastapi import FastAPI, HTTPException, Query
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel, Field

from environment import TASK_IDS, TrustSafetyAuditEnv
from inference import run_benchmark
from models import AuditAction, AuditObservation, AuditReward, Decision


# ═══════════════════════════════════════════════════════════════════════════════
# App Configuration
# ═══════════════════════════════════════════════════════════════════════════════

app = FastAPI(
    title="Trust & Safety Audit Environment",
    description=(
        "An OpenEnv environment simulating an Automated Trust & Safety Analyst "
        "workflow. The agent reviews LLM conversational turns and decides to "
        "ALLOW or BLOCK each turn to prevent prompt injection, jailbreaking, "
        "and data exfiltration."
    ),
    version="1.0.0",
    docs_url="/docs",
    redoc_url="/redoc",
)

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

# Global environment instance (one per server for HTTP endpoints)
_env = TrustSafetyAuditEnv()


# ═══════════════════════════════════════════════════════════════════════════════
# Request / Response Models
# ═══════════════════════════════════════════════════════════════════════════════

class ResetRequest(BaseModel):
    task_id: Optional[str] = Field(None, description="Task ID to run")
    seed: Optional[int] = Field(None, description="Episode seed for reproducibility")


class StepRequest(BaseModel):
    decision: str = Field(..., description="ALLOW or BLOCK")
    reasoning: Optional[str] = Field(None, description="Optional reasoning")


class ResetResponse(BaseModel):
    observation: Dict[str, Any]
    done: bool = False


class StepResponse(BaseModel):
    observation: Optional[Dict[str, Any]]
    reward: Dict[str, Any]
    done: bool
    info: Dict[str, Any]


class TaskInfo(BaseModel):
    id: str
    difficulty: str
    description: str
    expected_baseline_score: float
    expected_frontier_score: float


class TasksResponse(BaseModel):
    tasks: List[TaskInfo]
    action_schema: Dict[str, Any]


class GraderResponse(BaseModel):
    task_id: str
    score: float
    raw_score: float
    max_possible_score: float
    false_positives: int
    false_negatives: int
    turns_completed: int
    done: bool


class HealthResponse(BaseModel):
    status: str = "healthy"
    environment: str = "trust-safety-audit-env"
    version: str = "1.0.0"


class BaselineResponse(BaseModel):
    results: Dict[str, Any]
    status: str


# ═══════════════════════════════════════════════════════════════════════════════
# Endpoints
